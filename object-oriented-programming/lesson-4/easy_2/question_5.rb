# There are a number of variables listed below. What are the different types and how do you know which is which?

excited_dog = "excited dog"
@excited_dog = "excited dog"
@@excited_dog = "excited dog"

# excited_dog is a local variable as it is lower case and is no prefixed with an @
# @exceited_dog is an instance variable because it is prefixed with one @ symbol
# @@excited_dog is a class variable because it is prefixed with two @@ symbols
